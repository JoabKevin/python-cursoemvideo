# Exercícios com operadores aritméticos.
n1 = int(input('Digite um número: '))
n2 = int(input('Outro número: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
mo = n1 % n2
print(f'A soma vale {s}, o produto vale {m}, a divisão de {n1} por {n2} é {d}, a divisão inteira é {di}, o resto da divisão é {mo} e a potência é {e}.')
